package com.kranthiPra;

public class TypeCasting {
    public static void main(String[] args) {
        //converting one data type to another like Flot to Int
        // we can do this when two types are compatible like (Int and float) it should not be (chat and int or flot)
        //if the size is small then no need to do type casting ex : float can convert to Int without type casting
        int a = (int)(10.278f);
        System.out.println(a);

        float b= 10;
        System.out.println(b);// here no need to type cast




    }
}
